	<p class="text-primary"> <strong> ANIVERSARIO:</strong> 13 de septiembre</p>
	<p class="text-primary"> <strong> CLIMA:</strong> Templado</p>
	<p class="text-primary"> <strong> DISTANCIA:</strong>30 km.  </p>
	<p class="text-primary"> <strong> PARADA:</strong> Av. Barrientos y C. Barzola  </p>
	<p class="text-primary"> <strong> EXTENSIÓN:</strong>  74 km2  </p>
	<p class="text-primary"> <strong> FERIA SEMANAL:</strong> Jueves</p>
	<p class="text-primary"> <strong> FERIAS:</strong> Feria del Pato, Cuy y la Chicha Tolateña (1er domingo de julio)</p>
	<p class="text-primary"> <strong> FESTIVIDAD:</strong> Virgen de Patrocinio (13 al 16 de septiembre)</p>

<p class="text-muted"></p>
	<p class="text-muted">Es la Tercera Sección Municipal de la Provincia Germán Jordán, pertenece a la Región Valles. Es conocida como la Capital Turística del Valle Alto por su potencial en este rubro ofreciendo actividades y paisajes para todos los gustos. <br><br>
Tolata desde sus inicios fue un centro comercial, actividad que hizo que la población mantenga un moviendo económico importante, en el tiempo de la hacienda estaba ocupado por las familias Villegas, Aguirre, Barrientos, Aramayo, Abasto y otros, los mismos que al pasar el tiempo fueron vendiendo sus propiedades a piqueros, además que fueron afectados en 1945 con la construcción de la represa de La Angostura.
</p><br>
	<p class="text-primary"><strong>GASTRONOMÍA TRADICIONA</strong></p>
	<p class="text-muted">El municipio ofrece una gran riqueza cultural asociada a sus costumbres en cuanto a gastronomía se refiere. Si hablamos de comidas típicas podemos mencionar el plato del Pato en sus diferentes variedades de preparación y el Cuy en riquísimo (lambreado, chanka de conejo o la puta chanka). También podemos encontrar en la comunidad de Villa Copacabana Zona la Angostura, varios lugares en el transcurso de la carretera los platos a base de pescado como las Cabañitas, las Sardinitas, el Pejerrey y la Carpa, los mismos extraídos en su mayoría de la laguna de La Angostura. </p><br>

	<p class="text-primary"><strong>ATRACTIVOS TURÍSTICOS</strong></p><br>

	<p class="text-muted">El turismo representa un potencial económico significativo para el municipio, sus montañas, tiene bellezas naturales y vistas panorámicas. La economía de esta zona está basada en la agricultura y ganadería, en especial la crianza de corderos; del mismo modo, su suelo calcáreo es ideal para el cultivo de tuna.<br><br>
La Angostura está ubicada en el Municipio de Tolata a 18 Km, se encuentra un hermoso lago rodeado de restaurantes, donde también podrá realizar actividades como paseo en bote, pesca deportiva, motonáutica y si es posible el esquí acuático.
Iglesia Villa Copacabana,este sitio cuenta con un mirador, desde donde se puede contemplar la laguna con todo su esplendor y de la misma manera observar parte del municipio de Tolata.<br><br>
La artesanía y tejido tradicional a diferencia de otros, es una mezcla con diferentes colores y diseños, se destacan figuras geométricas y zoomorfas. 
</p>

