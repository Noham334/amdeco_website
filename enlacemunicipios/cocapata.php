<p class="text-muted"></p>
  <p class="text-muted">El Municipio de Cocapata es uno de los Municipios más jóvenes del departamento. Es la Tercera Sección Municipal de la Provincia Ayopaya y está situado en la Región Andina.<br><br>
Los ciclos productivos cortos permiten que se logre hasta 4 cosechas al año. En relación a la producción de papa y otros cultivos con rendimientos óptimos 1 cosecha al año.<br><br>
El Paisaje Incacasani en el municipio cuentacon encantos y junto a los parajes donde el inca dejo sus huellas en el pasado, en la actualidad todavía quedan algunas ruinas arqueológicas, las cuales evidencian los grandes recorridos que viajaban a lo largo y ancho del territorio Incaico, es así que dentro el municipio Cocapata en la parte de la región Altamachi se encuentran más de siete sitios de pinturas rupestres y restos arqueológicos.
</p><br>
</p><br>
  <p class="text-primary"><strong>VICUÑAS</strong></p>
  <p class="text-muted">Sería el único municipio de Cochabamba que cuenta con vicuñas, por lo mismo, se ha impulsado la capacitación de comunarios en la crianza y cuidado de las mismas. Ya que al momento de visitar este municipio se convierte en uno de los principales atractivos por la belleza de esta especie. Son considerados por los pobladores una e las joyas del lugar por adornar las colinas de las comunidades del municipio.<br><br> 
Cada año durante los meses de agosto y septiembre se realiza la captura de vicuñas a través de la Comunidad Manejadora de Vicuñas establecida legalmente en el municipio de Cocapata, esto con la finalidad de garantizar la permanencia de la especie en Cocapata, con actividades de captura, control sanitario, esquila y liberación con las comunidades anfitrionas realizan la captura de vicuñas.
</p><br>
  <p class="text-primary"><strong>ATRACTIVOS TURÍSTICOS</strong></p>
  <p class="text-muted">Laguna Naturales Wallatani ubicado a 3 horas desde la ciudad de Cochabamba; esta laguna es inmensa y cuenta con una gran cantidad de trucha y aves llamados Wallatas de gran tamaño de un color blanco resplandeciente.<br><br>
Caídas de agua con una altura de 100 metros, en temporadas de lluvia de noviembre a marzo se puede disfrutar de esta belleza, durante el resto del año disminuye la cantidad. Así también en esta región por la temporada de invierno las caídas de agua llegan a congelarse por completo como un velo de novia. 
Kalahuanca cuenta con 6 sitios diferentes de pinturas rupestres, que están distanciadas a 1 kilómetro, así también cuenta con muchos “chullpares”.<br><br>
Las pinturas rupestres de “Inca Kasani” (Aquí estoy inca) es uno de los sitios que cuenta con arte rupestre; tienen más de 2000 años de antigüedad, las mismas pertenecen a la cultura Incaica. Este atractivo está emplazado también entre dos lagunas naturales, con una exuberante riqueza de trucha y aves que inmigran en determinados periodos del año. <br><br>
