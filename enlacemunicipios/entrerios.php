<p class="text-muted"></p>
  <p class="text-muted">El municipio de Entre Ríos corresponde a la Sexta Sección Municipal de la Provincia Carrasco, pertenece a la Región del Trópico. <br><br>
Entre Ríos se encuentra en una de las regiones con mayor potencialidad en la producción de alimentos e hidrocarburos. Sus más de 30 mil habitantes se dedican a la actividad ganadera, piscícola y avícola, también al cultivo de granos, como el arroz, maíz, soya; frutas como la piña y el plátano  de exportación. Además, se produce café, papaya, sandías y otros.<br><br>
En el municipio se construyó la primera planta petroquímica en el país que produce amoniaco-urea; fertilizantes para la producción de alimentos, con una inversión de  más de 843 millones de dólares, como una forma de garantizar la seguridad alimentaria en Bolivia.

</p><br>
  <p class="text-primary"><strong>PARQUE NACIONAL CARRASCO</strong></p>
  <p class="text-muted">El Parque Nacional Carrasco, fue creado el 9 de diciembre de 1988. El 11 de octubre de 1991, a través del Decreto Supremo N° 229401.  <br><br>
Fisiográficamente forma parte de las zonas más bajas y ocupa el 42% del territorio del Municipio de Entre Ríos.  Las áreas protegidas cuentan con su propio plan de manejo, los cuales también  están a cargo del Gobierno Central a través de SERNAP. Esta unidad debe ser manejada con el objeto de proteger las cuencas por las fuentes de agua, vegetación, fauna silvestre, etc.

</p><br>
  <p class="text-primary"><strong>ATRACTIVOS TURÍSTICOS</strong></p>
  <p class="text-muted">Se cuenta con atractivos como el de Playa Alta que tiene cabañas para disfrutar del paisaje natural, la flora y fauna silvestre, ofrece actividades de paseos en botes, pesca deportiva y caza fotográfica. <br><br>
También se cuenta con el Mirador Rio Ichoa, desde donde se puede observarla gran variedad de flora. Paseos en Canoa, la diversidad de ríos existentes ofrece al visitante navegar por aguas cristalinas y a la vez una vista única de sus paisajes durante el trayecto.<br><br>
Poza Azul es otro lugar muy atractivo para los turistas, ubicado  en el Sindicato San Pedro,  presenta variedad flora y fauna, las actividades que se pueden realizar es  la cazas fotográficas del paisaje, natación,  paseos en botes y pesca deportiva.

