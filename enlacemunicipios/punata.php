	<p class="text-primary"> <strong> ANIVERSARIO:</strong> 18 de mayo</p>
	<p class="text-primary"> <strong> CLIMA:</strong> Templado</p>
	<p class="text-primary"> <strong> DISTANCIA:</strong> 50 km. </p>
	<p class="text-primary"> <strong> PARADA:</strong>   Av. República frente al Templo San Carlos</p>
	<p class="text-primary"> <strong> EXTENSIÓN:</strong> 178,17 km2  </p>
	<p class="text-primary"> <strong> FERIA SEMANAL:</strong> Martes</p>
	<p class="text-primary"> <strong> FERIAS:</strong> Feria de la Chicha y el Rosquete (2ª semana de mayo)</p>
	<p class="text-primary"> <strong> FESTIVIDAD:</strong> Señor de los Milagros (22 al 24 de septiembre)</p><br>

<p class="text-muted"></p>
	<p class="text-muted">Punata tiene principalmente producción agrícola. Los cultivos más importantes son el trigo, papa, avena, maíz, arveja, frutales como durazno, pera, manzana, ciruela, frutillas etc. La actividad agrícola de Punata es reconocida por la producción de peras, duraznos y otras frutas en huertos frutales. También se producen hortalizas y pasturas mejoradas de alto rendimiento, como la alfalfa. En las partes altas se cultiva variedades de papa, la papaliza, la oca, trigo y maíz.</p><br>
	<p class="text-primary"><strong>ROSQUETES PUNATEÑOS</strong></p>
	<p class="text-muted">En Punata, como herencia de saberes de un esplendoroso pasado, se continúa con la fabricación de roscas de harina bañadas con merengue, llamados  “rosquetes”. La repostería de “Rosquetes” es Patrimonio Cultural del Departamento Cochabamba desde el 2011. Son aros crujientes y dulces elaborados por los panificadores hechos en base a harina, huevo, manteca y azúcar.<br><br>
En Punata, además el maíz sigue siendo el príncipe de los cultivos del que se fabrica el néctar alcohólico amarillo (chicha) y el tinto (kulli).
</p><br>
	<p class="text-primary"><strong>ATRACTIVOS TURÍSTICOS</strong></p>
	<p class="text-muted">Entre los atractivos turísticos de Punata está el templo San Juan Bautista, que fue declarado Monumento Nacional debido a los importantes tesoros religiosos que custodiaba y sus antecedentes coloniales. El templo San Juan Bautista conserva tres enormes y magníficos retablos decorados en pan de oro del barroco andino clásico y refinado. Se trata de una parte de muchos encajes tallados en cedro.
<br><br>
En Punata es digna de visitar la “hacienda  Sivingani”, lugar donde las tropas patriotas se alojaron durante la época de las guerrillas por la independencia y algunos historiadores dicen que el Libertador Bolivar llego  también a alojarse en la hacienda Sivingani. La antigua hacienda Sivingani es una majestuosa edificación, con un largo aproximado de 20 metros y un alto de dos pisos, que data de 1740
</p>
	
