                      <br>
                      Pocoata, cantón del municipio ofrece una magia y encanto únicos. Esta hermosa campiña de la eterna primavera y de ensueño natural aún se conservan los antiguos molinos de piedra que funciona con la fuerza hidráulica, de sus cristalinas aguas, estas a su vez son utilizadas para la producción de papa, maíz, haba y otros cereales, que son comercializados en los principales centros de abasto del departamento.<br>
