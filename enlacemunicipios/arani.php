                    <p class="text-primary"><strong>PAN DE ARANI</strong></p>
                    <p class="text-muted">Sin duda el pan de Arani es un referente de Cochabamba ya que propios y extraños degustan y se enamoran de su exquisito sabor. Se dice que el agua y el viento son dos de los ingredientes principales, sin embargo, los panaderos suman uno clave, la harina de trigo preparada en los antiguos molinos de piedra que yacen en Pocoata, una campiña colonial que se halla a cinco kilómetros al sur y que es bañada por un riachuelo que nunca se seca y que según sus habitantes tiene el agua más pura de Bolivia. Son cinco molinos en los cuales los lugareños muelen el trigo. Las aspas y maquinaria vetusta son impulsadas por la fuerza de las aguas del río que baña Pocoata. Al parecer éste es el principal secreto del pan de Arani, sus molinos y su harina de trigo.
