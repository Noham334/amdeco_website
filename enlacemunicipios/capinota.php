</p>
  <p class="text-primary"> <strong> PARADA:</strong>  Av. Ayacucho y Av. 6 de Agosto
</p>
  <p class="text-primary"> <strong> EXTENSIÓN:</strong>571,6 km2
 </p>
  <p class="text-primary"> <strong> FERIA SEMANAL:</strong> Domingo
</p>
  <p class="text-primary"> <strong> FERIAS:</strong> Feria del Guarapo (Domingo de Resurrección)
 </p>
  <p class="text-primary"> <strong> FESTIVIDAD:</strong> Virgen de Asunta (14 al 16 agosto)
</p>

<p class="text-muted"></p>
  <p class="text-muted">Es la Primera Sección Municipal de la Provincia del mismo nombre, es además su capital, pertenece a la Región Valles. Capinota es conocida por su amplia y exquisita producción, llamada también la “Tierra del Guarapo”. <br><br>
La principal fuente productiva y económica en la zona es la agropecuaria. En la agrícola la producción de: Papa, zanahoria, maíz, trigo, tumbo, principalmente. La cría de animales para la producción lechera y la producción avícola como las principales.<br><br>
La explotación de agregados y de piedra caliza, son las principales actividades mineras, siendo COBOCE desde la década del 60’el principal eje productor.<br><br>
La producción y abastecimiento de frutas a las zonas mineras fue importante durante el siglo XX, de la misma manera la producción de uva proveía a muchas fábricas de vino de Cochabamba. El guarapo es la bebida típica de la zona, junto a la tomatada de pescado y el lambreado de conejo. 

</p><br>
  <p class="text-primary"><strong>RUTA DEL GUARAPO ARTESANAL</strong></p>
  <p class="text-muted">Se trata de una iniciativa para fomentar el turismo y realzar las potencialidades del municipio. El recorrido inicia en el cementerio general y visita a la cima de P’oqotayka, sitio ritual ancestral. Después de disfrutar de la vista de este mirador, se realizará una caminata por la montaña hasta llegar a la Pukara de P’irigallo, desde donde se apreciará el paisaje del Valle de Cucuni (Patrimonio Histórico y Cultural de Bolivia) y del pueblo de Irpa Irpa con su actividad industrial.<br><br>
Posteriormente, está programado compartir un “aphtapi”, comida comunitaria, con los comunarios de Cucuni que ofrecerán sus productos agrícolas. Las actividades culminan con la participación de los asistentes en la previa a la Feria del Guarapo Capinoteño. El domingo, se disfrutará de un desayuno tradicional  y se visitarán dos de los lugares más representativos de la viticultura artesanal de Capinota. Los participantes degustarán diferentes variedades de uva y sus derivados, principalmente el guarapo, el vino y el pico. También conocerán el proceso de elaboración de estas bebidas
</p><br>
  <p class="text-primary"><strong>ATRACTIVOS TURÍSTICOS</strong></p>
  <p class="text-muted">Serranía del Poqotayka, existencia de cerámica de tipo Tiwanaku, Tupuraya y Sierra Mokho Rosado. Chullpares en la Parte alta. Asentamientos datados desde 1100 a.c.
Molino de Capinota, construcción de la primera época de la colonia, aproximadamente del siglo XVI. Infraestructura con cuatro molinos de piedra para cereales. Con la explotación del Cerro Rico de Potosí se intensifican la producción de maíz y trigo, la demanda de harina incremento la construcción de molinos por ser el lugar estratégico. <br><br>
Molino y Hacienda de Cucuni, construido durante el primer tercio del siglo XX, pertenecieron al coronel Vitaliano Ledezma, veterano de la guerra del Acre y del Chaco. La construcción de los molinos se debió a los prisioneros paraguayos.<br><br>
Estación de Trenes de Buen Retiro, la apertura de las vías ferroviarias entre Oruro y Cochabamba significó la llegada de muchos aventureros extranjeros al valle Capinoteño, así también facilitó el transporte de productos agrícolas de Capinota a las zonas mineras

</p>
  

  
