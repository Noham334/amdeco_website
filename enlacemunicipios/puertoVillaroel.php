  <p class="text-primary"> <strong> CLIMA:</strong> Tropical húmedo
</p>
  <p class="text-primary"> <strong> DISTANCIA:</strong> 220 km.
</p>
  <p class="text-primary"> <strong> PARADA:</strong>  Av. Oquendo y Av. 9 de abril 
</p>
  <p class="text-primary"> <strong> EXTENSIÓN:</strong> 2.187 km2
 </p>
  <p class="text-primary"> <strong> FERIA SEMANAL:</strong> Domingo
</p>
  <p class="text-primary"> <strong> FERIAS:</strong> Feria Exposol (2ª o 3ª semana de abril)
 </p>
  <p class="text-primary"> <strong> FESTIVIDAD:</strong> Virgen Santa María de los Ángeles (1 al 3 de agosto)
</p>

<p class="text-muted"></p>
  <p class="text-muted">Es la Quinta Sección Municipal de la Provincia Carrasco y parte de la Región Trópico de departamento. Por su ubicación geográfica, está integrado al corredor de exportación hacia Rondonia en el Brasil<br><br>
Sus recursos hídricos son abundantes y sus vías de transporte terrestre y fluvial promueven la expansión de la frontera agrícola y el incremento del comercio. La sede de este municipio se encontraba en las orillas del río Ichilo. Las migraciones a esta zona, posibilitaron el crecimiento demográfico de otras comunidades, tal es el caso de Ivirgarzama. <br><br>
El río Ichilo es el principal atractivo del municipio, al ser el único río navegable de nuestro departamento, posee características importantes que permiten la actividad turística en función de actividades como la pesca deportiva, la observación de fauna y flora silvestres, el acceso a comunidades indígenas. Una de las especies de fauna más importantes, que está protegida debido a que se encuentra en peligro de extinción es el Bufeo (Inia Boliviensis), a quien es posible ver durante los paseos a lo largo del río.

</p><br>
  <p class="text-primary"><strong>POTENCIAL ECONÓMICO</strong></p>
  <p class="text-muted">Su economía es fundamentalmente agraria, seguida por la producción pecuaria, forestal, piscícola, el comercio y una incipiente industria. Los cultivos de importancia son el arroz, la yuca, banano, cítricos, coca, chirimoya, pina y maíz.<br><br> 
La explotación maderera y la pesca son otra fuente de ingresos importantes. En la localidad de Ivirgarzama, hay peladoras de arroz, aserraderos, empacadoras y. talleres artesanales. En el Municipio operan varias asociaciones de productores: tronqueros, pescadores, cocaleros, lecheros, de maracuyá y bananeros.

</p><br>
  <p class="text-primary"><strong>ATRACTIVOS TURÍSTICOS</strong></p>
  <p class="text-muted">Los principales atractivos de este municipio son: Los paseos acuáticos sobre el río Ichilo y sus afluentes. La región de San Benito donde existe un barranco a orillas del río, llamado “Lambedero” lugar donde diferentes especies de aves consumen lodo medicinal. En las riveras del frente, los comunarios habilitaron un mirador para observar la naturaleza en su real expresión.
En pleno centro de Puerto Villarroel se encuentra el “Clipper” Centro Turístico donde los visitantes pueden encontrar una variedad de artesanías típicas e información. 

</p>
  

  
