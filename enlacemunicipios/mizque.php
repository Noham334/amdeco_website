            <p class="text-muted"></p>
            <p class="text-muted">Mizque es la Primera Sección de la Provincia del mismo nombre, además de ser su capital, y uno de los municipios más antiguos del departamento. Pertenece a los municipios de la Región del Cono Sur. Es conocida como la “Ciudad de los Quinientos Quitasoles” en alusión a la época de la Colonia donde las damas paseaban por la plaza con sus quitasoles. <br><br>
              Su nombre proviene del vocablo quechua “Misk´i” que significa dulce, por la producción de miel en el municipio denominado también “Tierra Dulce” ya que en la región abundan las abejas silvestres, que hacen sus nidos en la tierra. <br><br>
              Llama la atención el encanto colonial con edificaciones conservadas, varias de las cuales tuvieron que ser refaccionadas luego del terremoto de 1998. Gracias al compromiso de su gente, la torre de la iglesia de San Sebastián, que había sido destruida, fue totalmente reconstruida a semejanza de la original.
            </p><br>
            <p class="text-primary"><strong>TIERRA DEL MANÍ Y LA CHIRIMOYA</strong></p>
            <p class="text-muted">Dos de los principales productos por los que se destaca el municipio son el maní, que se produce en gran escala con muy buena calidad, inclusive se cuenta con una planta que procesa y transforma este alimento como una forma de impulsar a los productores de la región. <br><br>
              Una de las frutas más exquisitas que se puede encontrar en Mizque es la chirimoya, que es muy apreciada por todos y tiene gran demanda en su época de cosecha. La Feria de la Fruta y Festival de la Pascua, realizada el sábado de Pascua, es un pequeño resumen de la variedad de productos del municipio donde se exponen estos dos productos además de granada, pacay, uva, miel, leche y en menor escala cítricos traídos desde las comunidades
            </p><br>
            <p class="text-primary">ATRACTIVOS TURÍSTICOS</p>
            <p class="text-muted">Uno de los potenciales más importantes en Mizque es el turismo ya que ofrece actividades para todos los gustos. Por un lado, en plena plaza principal se encuentra el Museo Arqueológico y Antropológico “Mauricio Valkanover” que muestra una completa colección de cerámicas, adornos, prendas y utensilios que resumen la rica tradición cultural de la región.<br><br>
              Paso de los Libertadores bello puente ubicado a 25 km. de Mizque, que se alza sobre un sendero incaico y muestra una imponente cañada justo donde se ubica este puente construido durante la colonia. El puente debe su denominación porque por allí hizo su paso Simón Bolívar durante la Guerra de la Independencia.
              Corrida de Toros, llevada a cabo durante su fiesta patronal del 8 al 15 de septiembre es una de las expresiones más auténticas de esta región y que por su antigüedad ha sido replicada en municipios más jóvenes de los alrededores. <br><br>
              Una historia singular es la de “Viña Perdida” un lugar donde actualmente se elabora un exquisito queso, pero que anteriormente había sido uno de los viñedos más importantes, el cual llegó a competir con los vinos españoles, por lo mismo se quemaron todos los viñedos en el sector por lo que se le asigno ese nombre.
              Otros atractivos turísticos son el sitio sagrado de Lakha Tambo, Cascada de Pajcha Pata, Fortaleza de Inca Pucarita, Pinturas Rupestres de Aguada y Taboada.
