 </p>
  <p class="text-primary"> <strong> FESTIVIDAD:</strong> San Miguel Arcangel  de Pojo (28 al 30 de septiembre)
</p>

<p class="text-muted"></p>
  <p class="text-muted">Es la Segunda Sección de la Provincia Carrasco, pertenece a la Región del Cono Sur. Por sus condiciones geográficas se divide en Pojo Valles y Pojo Trópico. Es conocida como la “Tierra de la Manzana” y como “Huerto de Mil Sabores” por su exquisita producción.
La zona de Pojo valles está conformada por regiones de montañas, colinas bajas y llanuras aluviales. Pojo trópico, en cambio, es una zona de transición entre pie de monte y llanos orientales como yungas cálido y selva amazónica.<br><br>
El municipio ofrece diferentes rostros a sus visitantes por una parte el centro poblado, pero también sectores predominantemente rurales. La presencia de varios pisos ecológicos en su territorio, valles y trópico, permiten la existencia de una flora y fauna muy variada, abundante y diferenciada en cada espacio agroecológico y ofrece hermosos paisajes. <br><br>
Asimismo, cuenta con recursos hidrocarburíferos abundantes que fueron transferidos a empresas transnacionales y que ahora están siendo explotados y recuperados para los bolivianos. La población es de origen quechua y en menor medida aymara. Los restos arqueológicos revelan que esta zona fue espacio de confluencia de las culturas andinas y amazónicas. 

</p><br>
  <p class="text-primary"><strong>POTENCIAL PRODUCTIVO</strong></p>
  <p class="text-muted">Pojo tiene una producción agropecuaria variada con utilización de maquinaria. Tiene tierras aptas para el pastoreo y para expandir su frontera agrícola. Sus productos son comercializados a través de la carretera antigua entre Cochabamba y Santa Cruz.<br><br>
El potencial del municipio está reflejado en su productividad frutícola son muy reconocidas su exquisitas manzanas y frutillas pero también producen durazno, ciruelo, guayaba, pacay, chirimoya, higo, uva, sandia, entre otros. El potencial agrícola sus principales productos son la papa, arveja, haba, achojcha, tomate, pimentón, maíz choclero, zapallo, locoto, poroto, oca, papalisa, trigo y cebada.

</p><br>
  <p class="text-primary"><strong>ATRACTIVOS TURÍSTICOS</strong></p>
  <p class="text-muted">Pojo cuenta con lugares muy interesantes para ser visitados por los turistas. Podemos mencionar “Orqoyuj Laguna” o lagunas de la cuna del Cerro. Las casas coloniales que se conservan en el pueblo son también un deleite a la visa, el árbol centenario ubicado en la Plaza 27 de mayo. También posee potencial forestal y para el turismo de aventura por la diversidad de especies naturales y paisajes.<br><br>
Las aguas termales y el cañadón del río Pojo, Cascadas de Cóndor Pata, el Cajón del Diablo y las Ruinas Arqueológicas son solo un poco de la variedad que ofrece Pojo en cuanto a turismo se refiere. 

</p>
  

  
