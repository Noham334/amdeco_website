	<p class="text-primary"> <strong> Municipio:</strong>Tarata</p> 
	<p class="text-primary"> <strong> CARACTERÍSTICA:</strong>Cuna de Próceres</p>
	<p class="text-primary"> <strong> FUNDACIÓN:</strong> 24 de noviembre 1914</p>
	<p class="text-primary"> <strong> ANIVERSARIO:</strong> 14 de noviembre</p>
	<p class="text-primary"> <strong> CLIMA:</strong> Templado </p>
	<p class="text-primary"> <strong> DISTANCIA:</strong> 35 km.  </p>
	<p class="text-primary"> <strong> PARADA:</strong> Av. Barrientos y C. Guayaramerin</p>
	<p class="text-primary"> <strong> EXTENSIÓN:</strong>375,63 km2</p>
	<p class="text-primary"> <strong> FERIA SEMANAL:</strong> Jueves</p>
	<p class="text-primary"> <strong> FERIAS:</strong> Feria del Chorizo, Chicha y Aloja Tarateña (1ª semana de julio) </p>
	<p class="text-primary"> <strong> FESTIVIDAD:</strong> San Severino (24 al 26 de noviembre)</p><br>

	<p class="text-muted">Es la Primera Sección Municipal de la Provincia Esteban Arce, capital de provincia, que corresponde a la Región de los Valles. Es conocido y catalogado localmente como la cuna de Hombres Históricos de Bolivia.<br><br>
Esta ciudad colonial del Valle Alto denominada también como la “Villa Colonial”  muestra a través de sus calles, casas coloniales, templos y museos la historia, no sólo del pueblo sino del país, porque fue de aquí de donde emergieron dos expresidentes como son Mariano Melgarejo y René Barrientos cuyas casa aún permanecen y forman parte del atractivo de la ciudad.
</p><br>
	<p class="text-primary"><strong>ALFARERÍA Y TRADICIONES PRINCIPALES</strong></p>
	<p class="text-muted">A 2 km. del centro poblado se encuentra el cantón de Huayculi dedicado casi enteramente a la alfarería la cual es considerada una actividad familiar, este trabajo es su principal fuente de ingreso, con gran demanda a nivel departamental y nacional. Los comunarios se dedicaron a esta actividad por la abundancia de la arcilla en sus tierras. El trabajo se realiza en tres maneras diferentes, que son el moldeado, torneado y esculpido, cuya cocción lo realizan en hornos de barro, incursionando actualmente en la cerámica esmaltada utilizando hornos eléctricos.<br><br>
La producción de chicha es una tradición tarateña, y muchas familias se dedican a esta actividad, así también la gastronomía a través de la elaboración y venta de Chorizo Criollo plato típico de esta región; ambas actividades han impulsado la feria de la chicha y chorizo criollo en este municipio.
</p><br>
	<p class="text-primary"><strong>ATRACTIVOS TURÍSTICOS</strong></p>
	<p class="text-muted">El templo de San Pedro, ubicado cerca de la Plaza de Armas, tiene la estructura de una sola nave en forma de cruz latina. Sus retablos están tallados en madera y cubiertos con pan de oro. En este sitio se conserva el primer libro de fábrica (1963) y también un órgano del siglo XVIII considerado reliquia patrimonial de Tarata, al lado derecho del interior del templo, se puede apreciar la espada original de Melgarejo.
Tarata también posee el convento Franciscano San José que es uno de los más antiguos de Bolivia creado por orden de Carlos IV de España en 1792 para la formación de misioneros.<br><br>
Casa del Gral. Mariano Melgarejo lugar donde nació este controvertido personaje de nuestra historia, la misma conserva parte el bloque original, construcción de adobe del siglo XVIII, en sus ambientes se encuentra el parque infantil y para apoyar la parte forestal del municipio el vivero municipal tiene proyectado la reconstrucción de estos recintos para eregir el museo de los presidentes nacidos en Tarata.
</p>

