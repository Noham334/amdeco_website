	<p class="text-primary"> <strong> CLIMA:</strong> Fresco a Templado</p>
	<p class="text-primary"> <strong> DISTANCIA:</strong> 119 km.  </p>
	<p class="text-primary"> <strong> PARADA:</strong> C. Mairana y Av. República  </p>
	<p class="text-primary"> <strong> EXTENSIÓN:</strong>  603 km2 </p>
	<p class="text-primary"> <strong> FERIA SEMANAL:</strong> Domingo</p>
	<p class="text-primary"> <strong> FERIAS:</strong> Fiesta Autóctona (3 domingo de agosto)</p>
	<p class="text-primary"> <strong> FESTIVIDAD:</strong> Virgen Mestiza de Sik´imira (23 al 25 de septiembre)</p><br>

<p class="text-muted"></p>
	<p class="text-muted">Es la Segunda Sección Municipal de la Provincia Mizque, pertenece a la Región del Cono Sur. Su nombre real es “Villa Eufronio Viscarra” en justo homenaje a un personaje destacado por ser un gran político e historiador de la Provincia. <br><br>
El mayor tesoro turístico de Vila Vila, encuentra en sus zonas paleontológicas, arqueológicas, monumentos naturales y diversas costumbres socioculturales.
La principal actividad productiva es la agropecuaria, con cultivos de papa, oca, papaliza, camote, trigo, maíz, cebada, avena, quinua, linaza, haba, arveja, tarwi, cebolla, tomate, ají, maní; y la crianza de ganado ovino, caprino y la avicultura. La mayor parte de la producción se destina al consumo doméstico y una pequeña cantidad se comercializa en las ferias municipales y en las de Mizque.<br><br>
La producción de chicha y de tejidos artesanales también está destinada al consumo familiar. En Vila Vila hay yacimientos de plomo y zinc que no han sido explotados industrialmente.
</p><br>
	<p class="text-primary"><strong>PALEONTOLOGÍA </strong></p>
	<p class="text-muted">Si bien el municipio de Vila Vila es poco conocido en el ámbito turístico y paleontológico, desde hace 45 años es objeto de constantes trabajos de investigación paleontológica, seguido por la parte arqueológica, en los que se destacan los estudios realizados por Leonardo Braniza, que dieron como resultado el hallazgo de varios fósiles de tortugas y cocodrilos en el sector de T’iupampa.<br><br>
Estos hallazgos motivaron a que el mundo científico dirigiera su mirada a esta región, lo que hizo posible el descubrimiento de otros fósiles de ranas y peces Holosteos y Telesteos, además de mamíferos distribuidos en 20 especies
</p><br>
	<p class="text-primary"><strong>ATRACTIVOS TURÍSTICOS</strong></p>
	<p class="text-muted">Los Portales de la Media Luna, especie de esculturas talladas por el viento en inmensos bloques de arenisca  roja.
Los Valles de Marte, una pradera ondulada que desciende al valle que da la impresión de  estar en otra parte del mundo: grandes peñascos de granito gris oscuro exhiben sus formas caprichosas, sus marcas inexplicables, sus perforaciones enigmáticas. <br><br>
Chilipata (restos, fósiles pre - históricos de animales), Tejidos Artesanales, Antiguas vías del tren son algunos de los atractivos del municipio.
</p>
	
