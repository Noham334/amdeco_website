                Pocona es un cofre de historias, leyendas y recuerdos que a través del tiempo permanecen reflejadas en los diferentes destinos turísticos. Cerros sombríos e inmensos que se agrandan a medida que la vista recorre y al pie un hermoso valle, forma un cuadro lleno de vida donde el umbroso follaje de durazneros, ceibos, eucaliptos y alisos, nos invita a detener la marcha para contemplarlo, su cielo azul y sus colinas cubiertas de flores silvestres constituyen el encanto especial de Pocona. <br><br>
                Batalla de Quehuiñal, llevada a cabo el 23 y 24 de mayo de 1812 en el valle de Pocona, donde un ejército patriota liderado por don Esteban Arce, disminuidos en armamento, pero con el valor en la mente y el corazón, lucharon con convicción y heroísmo con el único propósito de terminar con la esclavitud y detener a las tropas de Goyeneche.

