	<p class="text-primary"> <strong> Municipio:</strong> Sacabamba</p> 
	<p class="text-primary"> <strong> CARACTERÍSTICA:</strong> “La Tierra del curaca Martín Uchu” </p>
	<p class="text-primary"> <strong> FUNDACIÓN:</strong> 1 de Noviembre de 1990 </p>
	<p class="text-primary"> <strong> ANIVERSARIO:</strong> 25 de Abril </p>
	<p class="text-primary"> <strong> CLIMA:</strong> Templado </p>
	<p class="text-primary"> <strong> DISTANCIA:</strong> 	 62.2 km. </p>
	<p class="text-primary"> <strong> PARADA:</strong> Av. República y C. Mairana   </p>
	<p class="text-primary"> <strong> EXTENSIÓN:</strong>   188 km² </p>
	<p class="text-primary"> <strong> FERIA SEMANAL:</strong> No tienen</p>
	<p class="text-primary"> <strong> FERIAS:</strong> Expofrut (1ª semana de julio)</p>
	<p class="text-primary"> <strong> FESTIVIDAD:</strong> Virgen de Urkupiña (23 al 25 de agosto)</p><br>

<p class="text-muted"></p>
	<p class="text-muted">Sacabamba es la cuarta sección municipal de la provincia Esteban Arze. Se encuentra ubicado al sudeste del departamento a una distancia de 43 kms. de la ciudad de Cochabamba. Limita al norte con el municipio Anzaldo y Villa Rivera, al sur con Punata, al este con Mizque y al oeste con Anzaldo. Se encuentra a una altura que va de los 2.500 a los 3.700 msnm. Su topografía es variada, con serranías onduladas, pendientes y terrenos escarpados. El clima es templado con una temperatura promedio de 12°C.

Los ríos, quebradas y torrenteras del Municipio pertenecen a la cuenca del Valle Alto que por medio del río Sulti desembocan en el Jatun Mayu que a su vez es parte de la cuenca del río Siches, principal afluente de la laguna de la Angostura. Los suelos son frágiles y con susceptibilidad a los procesos de erosión laminar.
</p><br>
	<p class="text-primary"><strong>LA TIERRA DEL CURACA MARTIN UCHU </strong></p>
	<p class="text-muted">Durante el tiempo de la colonia este territorio era dependiente de Tarata y se le conocía como “La Doctrina y Vice Parroquia". En época de la insurrección el cacique Martín Uchu, el 28 de febrero de 1781 un Miércoles de Ceniza durante la noche congregó una gran masa de gente en el poblado. Martín Uchu el curaca de la Hacienda de Sacabamba dirigió el alzamiento sublevando a los indios precedidos por el ulular de los pututos, de los que se dice que estaban armados con garrotes, instrumentos de labranza, hoces, lanzas y algunas armas de fuego sustraídas de las haciendas, de esta manera de los terratenientes huyeron, solo quedaron algunos que más tarde fueron asesinados.</p><br>
	<p class="text-primary"><strong>ATRACTIVOS TURÍSTICOS</strong></p>
	<p class="text-muted">Es importante destacar la represa de Challaque por sus plantaciones y los bosques de Yuncataki, repletos de pinos. También es de interés el mirador del Calvario del Niño Jesús de Prada. Puede ser interesante visitar el mercado, donde los jueves de cada semana se pueden adquirir alimentos en la plaza. El carril-bus también representa una buena alternativa para realizar turismo por toda la zona.</p>
	
