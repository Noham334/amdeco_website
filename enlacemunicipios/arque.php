<p class="text-muted"></p>
  <p class="text-muted">Arque es la primera sección municipal de la provincia del mismo nombre y está ubicada en la Región Andina, cuenta con serranías elevadas que contrastan con la profundidad de sus barrancos, lo que representa un espectáculo impresionante para todos los visitantes. <br><br>
Las laderas de los cerros se asemejan a cuadros de colores por los tipos de arena, los pisos geológicos que presenta y su variado clima, hacen de estos paisajes una experiencia única.
Arque de acuerdo a los datos históricos sería uno de los municipios más antiguos del país. Antiguamente era una estación de trenes en el trayecto entre Cochabamba y Oruro, cuando funcionaba  la empresa nacional de ferrocarriles ENFE de Bolivia, hasta que el año 1996 fue transferida a capitales privados y al ser un tramo con muchos derrumbes que arruinaban la línea férrea se decidió discontinuarla  y actualmente el tránsito es por tierra.  
