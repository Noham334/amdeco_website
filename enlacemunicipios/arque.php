La región se dedica principalmente al cultivo de papa, trigo, cebada y en menor proporción a las hortalizas, por otra parte los pobladores también realizan  la crianza de ganado ovino y camélido. 
</p><br>
  <p class="text-primary"><strong>DATO CURIOSO</strong></p>
  <p class="text-muted">Arque también es conocido como un sitio histórico por la trágica muerte del General René Barrientos Ortuño, presidente de la República de Bolivia,  a la edad de 50 años. Quien después de concluir sus actividades presidenciales, se disponía a marcharse en su helicóptero, lamentablemente pocos minutos después de despegar la hélice terminó enredándose en un cable telegráfico que provocó la caída estrepitosa y el inmediato incendio de la nave el 27 de abril de 1969.
</p><br>
  <p class="text-primary"><strong>ATRACTIVOS TURÍSTICOS</strong></p>
  <p class="text-muted">Entre sus atractivos turísticos más importantes están las zonas arqueológicas, la “Tumba del General”, el río Arque y las aguas termales. El Bosque Encantado en Berenguela y las Aguas Termales Cabra Cancha y Colcha.<br>
    <br>
Asimismo se destacan las ruinas de Inca Pucara (Callajchullpa) y la majestuosidad del río Arque, donde se puede apreciar el exuberante paisaje valluno.<br><br>
Los productos artesanales propios de la cultura y tradición del municipio (prendas de vestir, herramientas agrícolas, alfarería y otros) pueden convertirse en un gran atractivo turístico.

