	<p class="text-primary"> <strong> ANIVERSARIO:</strong> 4 de septiembre</p>
	<p class="text-primary"> <strong> CLIMA:</strong> Templado</p>
	<p class="text-primary"> <strong> DISTANCIA:</strong> 17 km.  </p>
	<p class="text-primary"> <strong> PARADA:</strong> Av. Aroma y Av. San Martin   </p>
	<p class="text-primary"> <strong> EXTENSIÓN:</strong> 209,56 km2  </p>
	<p class="text-primary"> <strong> FERIA SEMANAL:</strong> Lunes</p>
	<p class="text-primary"> <strong> FERIAS:</strong> Feria de la Manzana (3 domingo de abril)</p>
	<p class="text-primary"> <strong> FESTIVIDAD:</strong> Virgen del Rosario (8 al 10 de octubre)</p><br>

<p class="text-muted"></p>
	<p class="text-muted">Es la Cuarta Sección Municipal de la Provincia Quillacollo, es parte de la Región Metropolitana. Conocida como la “Gran Manzana de Bolivia” por su potencial en cuanto a producción de la variedad de manzana camuesa. 
<br><br>
El escudo de Vinto recoge las características y las seis potencialidades del municipio: el pico de la cordillera del Tunari, el recurso del agua, la producción de manzana camuesa, el choclo, el ganado vacuno y los ferrocarriles.<br><br>
La actividad económica de Vinto gira en torno a la agropecuaria. Los principales cultivos son la papa, maíz, hortalizas y los frutales destacando las manzanas y los duraznos. La ganadería lechera, la cría de aves y la porcinocultura, son las principales actividades pecuarias. La producción artesanal de cestería y cueros es otra actividad destacable. Por su ubicación en la zona de tránsito de la carretera Cochabamba - La Paz, las actividades comerciales y de servicios constituyen otra fuente de generación de ingresos para la población. Los centros recreacionales, como balnearios y restaurantes, reciben una afluencia de visitantes de la ciudad de Cochabamba.

</p><br>
	<p class="text-primary"><strong>VILLA ALBINA</strong></p>
	<p class="text-muted">Al norte se encuentra la hacienda Villa Albina de Pairumani, construida el año 1925 por Simón I. Patiño, famosa por sus jardines de influencia asiática, árboles ornamentales exóticos, convirtiéndose así en uno de los monumentos más importantes y atractivos de la región. <br><br>
Es una de las haciendas  más grandes del valle de Cochabamba. Sus imponentes instalaciones para la investigación agrícola y la producción ganadera; la casa señorial de Villa Albina, convertida hoy en museo; la planta generadora de electricidad y el mausoleo de la familia Patino, son lugares frecuentados por visitantes durante todo el año. En Pairumani funciona el Centro de Investigaciones Fitogenéticas, que conserva el principal banco genético de maíz de Bolivia.
</p><br>
	<p class="text-primary"><strong>ATRACTIVOS TURÍSTICOS</strong></p>
	<p class="text-muted">Cuenta con varios atractivos turísticos principalmente, El parque ecológico de Pairumani nos ofrece bosques de eucaliptos aptos para el paseo y el camping, dentro de este parque se encuentran los servicios de un restaurant típico, una cascada natural y campos deportivos para la recreación. En este lugar funcionaba una estación hidroeléctrica que pertenecía al Varón del Estaño.<br><br>
La comunidad de Combuyo se encuentra en la ladera norte de Pairumani, esta zona se caracteriza por la excelente producción agrícola de hortalizas, las cuales proveen a los mercados centrales de Cochabamba.
</p>
	
