          <p class="text-muted">El escenario de esta fuente se encuentra en la rinconada o “cuesta kuhu”, a cinco cuadras de la plaza principal del pueblo, sobre la calle Vandiola. La misma que se encuentra cobijada por un marco de árboles y pastizales. En este sector también la llaman la “bóveda”, por las características arquitectónicas de la estructura de protección que se armó alrededor del manantial.<br><br>
            Cuentan los pobladores que para ellos el amor es de color turquesa porque precisamente ese es el color que adquiere el agua al emanar a la superficie. Las aguas de la Fuente, mencionan, son medicinales y son capaces de curar todo tipo de dolencias, en especial las del corazón. Dicen también, que al tomar de esta agua el hombre se hace más viril y la mujer más fecunda, y por esa característica es que los primeros habitantes de la zona la bautizaron como tal. Aunque también existen aquellos que suelen lanzar una moneda al interior de la fuente solo para pedir el regalo del amor.
          </p><br>
