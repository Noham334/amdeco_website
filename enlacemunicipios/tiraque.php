            Otros atractivos que oferta el municipio son: Ch´ejta Rumi, Árbol Centenario patrimonio natural y Q´omer Khocha; Represas de Pachaj Khocha, Millu Khocha, Murumuntani, Totora Khocha, Koari Kewiña y Kewiña Khocha, Infernillos, Yanakhocha, Kaspicancha, Yuska Khocha, Mulu Loma y Th´otorayuj. Lagunas: Yanakhocha, Kayaranioj, Chaupi Khocha, Salla Khocha, Q´omer Khocha.
