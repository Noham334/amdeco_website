	<p class="text-primary"> <strong> Municipio:</strong>Toco</p> 
	<p class="text-primary"> <strong> CARACTERÍSTICA:</strong> Tierra del Pan Chhama </p>
	<p class="text-primary"> <strong> FUNDACIÓN:</strong> 3 de abril 1957 </p>
	<p class="text-primary"> <strong> ANIVERSARIO:</strong> 3 de abril</p>
	<p class="text-primary"> <strong> CLIMA:</strong> Templado</p>
	<p class="text-primary"> <strong> DISTANCIA:</strong>Templado</p>
	<p class="text-primary"> <strong> PARADA:</strong>   Av. Barrientos y C. Barzola</p>
	<p class="text-primary"> <strong> EXTENSIÓN:</strong> 86,56 km2  </p>
	<p class="text-primary"> <strong> FERIA SEMANAL:</strong> Viernes</p>
	<p class="text-primary"> <strong> FERIAS:</strong> Feria del Pan de Toco, Comida tradicional y miel (1 domingo de abril)</p>
	<p class="text-primary"> <strong> FESTIVIDAD:</strong> Señor de Toco (1 fin de semana de julio)</p>

	<p class="text-muted">Toco es la Segunda Sección Municipal de la Provincia German Jordan, corresponde a la Región Valles. 
El pueblo de Toco es tan antiguo que, según la historia, el año 1576 después de la fundación de la capellanía del lugar por el primer Cura y Vicario de la Villa de Oropeza Dn. Antonio de Castro, dio origen a un poblado en esta región. A la cual se suma el nombre de la persona que se preocupó por su transformación y urbanización de este rincón del Valle en un pueblo atrayente.<br><br>
Toco era imponente el siglo pasado, en su aspecto físico sereno y apacible con huellas del pasado, sus casonas añejas techos de teja de barro, unas más imponentes que otras, dos pisos, balconaje de madera y tallado colonial. Son reminiscencias de antaño que se agregan a la historia y constituyen el pasado de un pueblo engalanado de hermosas tradiciones.
</p><br>
	<p class="text-primary"><strong>TEMPLO DEL SEÑOR DE TOCO</strong></p>
	<p class="text-muted">Considerado reliquia colonial, debiera ser declarado Monumento Nacional; presenta características de sumo interés y atrayente estilo, con una hermosa cúpula policromada, abovedada con loza cocida de lustre multicolor, obra de estilo Bizantino. Que, en cumplimiento de los santos sacramentos de la Religión Católica, pasaron por este rincón, personajes de la Historia Nacional, como Mariano Melgarejo.</p><br>
	<p class="text-primary"><strong>ATRACTIVOS TURÍSTICOS</strong></p>
	<p class="text-muted">Los molinos de piedra situados al borde de los ríos Toco y Siches que se usan para conocer el apetecido pan de chama son atractivos de la zona. También existen áreas turísticas que son poco aprovechadas como ser: los bateones de agua en el río Siches, la represa de Chua Loma, los molinos de piedra, el templo, la arquitectura urbana entre otros. En el municipio se encuentra una antigua iglesia, San Miguel, considerada una joya arquitectónica construida en 1788, lleva la cúpula recubierta de cerámica multicolor, hecha por artesanos de Huayculi. En esta iglesia fue bautizado el expresidente Mariano Melgarejo.


	</p>

	
	

	
