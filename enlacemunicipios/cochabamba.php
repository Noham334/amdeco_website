                <h5><p class="text-primary">Ubicación Geográfica</h5>
            
         El municipio de Cercado forma parte de las 16 provincias existentes en el Departamento de
         Cochabamba, encontrándose casi en el centro del mismo. Tiene una superficie de 55.631 Km2
         y forma parte de la subregión del valle central junto con los municipios de Sacaba, Quillacollo,
         Colcapirhua, Tiquipaya, Vinto y Sipe Sipe. El municipio tiene una altitud promedio de 2.500
             m.s.n.m.<br><br>
         
              <h5><p class="text-primary">Los límites del municipio son:</h5>
         
          El Límite Norte se encuentra en la cordillera del Tunari, con los municipios de Tiquipaya y
         Sacaba.<br><br>
          Al Este los límites, con el municipio de Sacaba, son la quebrada Chaqui Mayu, las
         serranas de San Pedro, los cerros del Abra y San Cristóbal hasta los cerros de Uspha
             Uspha.<br><br>
          Al Sur el límite empieza en la serrana de Lazo Rancho y con el límite de los municipios de
         Arbieto, Santivañez y Sacaba.
         Al Oeste desde Kaspi Chaka continuando hasta el límite por la quebrada de Rumi Mayu
         confluyendo en la quebrada Taquiña, limitando con los municipios de Tiquipaya,
         Colcapirhua y Quillacollo.<br>
              <pre>
             
           <strong>COMUNAS</strong>
          La Comuna Tunari comprende los distritos 1, 2 y 13 (zona norte).
          La Comuna Molle comprende los distritos 3 y 4 (zona oeste)
          La Comuna Alejo Calatayud comprende los distritos 5 y 6 (zona sud)
          La Comuna Valle Hermoso comprende los distritos 7, 8 y 14 (zona sudeste).
          La Comuna Itocta comprende  el distrito 9 (zona sud).
          La Comuna Adela Zamudio que comprende los distritos 10, 11 y 12 (zona céntrica y
         este).
         </pre>
         <br>
             </p>
                   
                   </div>
                   
               <p class="text-muted">
                 <h3><P class="text-primary">Datos Generales</h3>
         
         SUPERFICIE: 1,150 Km2
         ALTITUD: 2400 - 2900 m.s.n.m.
         RELIEVE: Caracterizado por: Terrazas, Valles, Pie de monte y Serrañas
         TOPOGRAFIA: Se caracteriza por tres grandes paisajes fisiográficos: una fracción de la parte sur del Parque
                 Nacional Tunari, el propio Valle Central y la Serranña de San Pedro, en la parte sur.<br><br>
                 
           <h5><p class="text-primary">ZONAS FISIOGRAFICAS:</h5>
          PERIMETRO NORTE: Cordillera (Parque Tunari), Pie de Cordillera 2750 a 2900 m.s.n.m.,
         Sector Urbano Noroeste.
         PERIMETRO CENTRAL: Sector Urbano Sudoeste, Pie de Serrana, Serrana Este
         PERIMETRO SUR: área Agrícola, Pie de Serrana, Serrana Sud
         CLIMA: Esté definido por dos estaciones claramente definidas: el verano, que se da en los meses de
         diciembre, enero y febrero; y el invierno, que se da en los meses de junio, julio y agosto
         TEMPERATURA: Máxima Media Ambiente: 26,80C
         Media Ambiente: 17,95C
           Mánima Media Ambiente: 9,00ºC<br>
            <h5><p class="text-primary">RECURSOS NATURALES:</h5>
                 
         
         SUELOS: Tierras de uso silvopastoril limitado, debido a que se encuentran en pendientes
         elevadas y sufren escasez de agua. En la parte baja del valle se ubican las mejores tierras
         agrícolas, que sufren también debido a la escasez de agua, la consiguiente salinización del
         suelo y a la presión de los asentamientos humanos.<br><br>
         FLORA: Las especies forestales nativas: tipa, kacha kacha, quebracho, mara, molle,
         jacarandó, retama, sunchu, cacahueca, chilca, sauce llorán.<br>
         Entre las especies forestales exíticas o introducidas tenemos: eucalipto, llamo, ceibo, pino
         radiata y algunas acacias que, sin ser nativas, se adaptaron a la region.
         FAUNA: Se tiene entre la fauna acustica al platincho, suche, arcoiris, carpa, caracho. Entre la
         fauna terrestre se tiene a la carachupa, zorro, zorrillo, oso andino, hurán, puma, gato andino,
         urina, liebre, vizcacha, kara maqui y entre la aves jilguero, la perdiz pequeña, perdiz colorada,
         palomas, garza oscura, pato cuervo, gallinazo, carcaca, paraba, frente roja, khechichi, loro
            andino y entre los reptiles la iguana, peni, coral
         
            <h5><p class="text-primary">RECURSOS MINERALOGICOS: </h5>
         La Microregión del Valle Central cuenta con limitados
         recursos minerales metálicos con pequeños yacimientos de plomo, plata, hierro, oro y piedras
         preciosas. En el Valle Central predominan las arcillas, siendo los yacimientos de mayor
         importancia los del río Rocha (junto con algunas quebradas menores).
         También están disponibles las arcillas en la zona de Champa Rancho y La Tamborada,
               agregados en Chakimayu y piedra caliza en Kara Kara y Uspha Uspha.
               <br>
               </p>
      
