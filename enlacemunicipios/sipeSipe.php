            EL Chilijchi de 160 años de la plaza principal, es ahí donde se remonta la historia de Sipe Sipe, en este lugar se realizaron sacrificios de personas que intentaron invadir el pueblo. Este chilijchi contiene también la historia del nombre del municipio que proviene del quechua “Sipina” que significa ahorcar ya que la gente mencionaba “sipiq, sipiq” cuando capturaban a algún español que trataba de invadir la población y procedían a horcarlo en el chilijchi de la plaza.
