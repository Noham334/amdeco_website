          <p class="text-muted">El municipio en la Segunda Sección de la Provincia Arque, está ubicado en la Región Andina. Fue el primer municipio del país en promulgar su Carta Orgánica consolidando así su autonomía. <br><br>
            Está rodeado de dos caudalosos ríos, en cuyas orillas están asentadas las comunidades. Dando al lugar un paisaje muy particular. Posee una arquitectura típica de la colonia, las calles del principal poblado son estrechas y empedradas, las casas están construidas con adobe y techos de paja.
