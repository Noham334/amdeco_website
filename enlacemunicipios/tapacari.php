          <p class="text-muted"><strong><i>Aymara,</i></strong> comprenden los distritos Challa y Leque en cuanto a música y danza en Challa se caracterizan las qonqotas y los tabla charangos, la tolomeada en época de lluvias y los lichiwayos, la zampoña, los suri sicuris y jula julas en época seca o cosecha. Y en el distrito Leque la tradicional moseñada en época de lluvia y la sicureada y paceñada en época seca.
            <strong><i>Quechua,</i></strong> comprenden los distritos de Tapacarí, Tunas Vinto y Ramadas en cuanto a la música y danza es similar por lo que en época de lluvias se predomina la moseñada y la charangueada acompañado de la concertina y en época seca la zampoñada y sicureada.
