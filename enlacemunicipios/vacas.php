          <p class="text-muted">Es la Segunda Sección de la Provincia de Arani, pertenece a la Región del Cono Sur, es una de las poblaciones más antiguas del Valle Alto. Por las pampas de Vacas el patriota Esteban Arce habría pasado con sus hombres hacia Pocona lugar donde se llevó a cabo la batalla del Quehuiñal el 24 de mayo de 1812.<br><br>
            Sobre el origen del nombre de Vacas, según la tradición oral, se debió a la presencia del ganado vacuno que pastaban en las proximidades de las lagunas, tal como ocurre actualmente. Otra versión señala que en las proximidades donde se asienta la población de Vacas, durante el periodo pre colonial era una “waca” o lugar sagrado. De ahí provendría el nombre. Se cuenta que los incas antes de ingresar a Incallajta, ciudadela incaica ubicada en Pocona, sagradamente debían rendir pleitesía a sus seres tutelares en las denominadas wacas.<br> <br>
            Este encantador municipio está rodeado de cerros que parecen alcanzar las nubes, en cuyos pies se esparcen enormes campos verdes y praderas bañadas de eternas lagunas pesqueras de color turquesa esmeralda en donde ganado vacuno y ovino pastan. Se destaca también por la producción de papa y la pesca del pejerrey que son una muestra del potencial del municipio, sus pescados son muy apetecidos por propios y extraños por su exquisito sabor.
          </p><br>
          <p class="text-primary"><strong>PUYA RAYMONDI</strong></p>
          <p class="text-muted">La Puya Raymondi de todas las plantas nativas del municipio es la más destacada, conocida también como Titanca, la cual llega a medir hasta 4 metros de altura y florece cada cien años, la cantidad de flores son de 5000, después de soltar sus semillas estos mueren y retoñan sus nuevos plantines.
          </p><br>
          <p class="text-primary"><strong>ATRACTIVOS TURÍSTICOS</strong></p>
          <p class="text-muted">Vacas cuenta con 2 cascadas permanentes una denominada Toro Warkhu y otra llamada Rosas Pampa. Además de 6 increíbles lagunas: Porko K´ocha (ubicada en el ingreso a Vacas), Acero K´ocha, Pilahuito, Juntutuyu, Qollpa K´ocha y Yanatama. <br>
            <br>
            A 35 km. Del centro poblado se encuentra la zona arqueológica “El Entierro” donde antiguamente los incas sepultaban a sus muertos y es considerado un sitio sagrado.
            Otro de los atractivos más resaltantes del municipio es el Complejo Turístico “Toro Warkhu” ubicado a 8 km. del centro, la misma cuenta con cabañas para una capacidad de 54 personas y espacios de recreación y camping. Es un lugar extraordinario ya que se encuentra a escasos metros de la Cascada de Toro Warkhu.
